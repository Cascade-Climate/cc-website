    <h2>Key Recommendations from Foundations</h2>

    <h3>1. Account for all terms of the net carbon balance&nbsp;</h3>

    <p><em>Foundations</em> provides a clear framework for measuring and calculating net carbon dioxide removal (CDR) from an ERW project. Net CDR is calculated by measuring potential carbon dioxide removal in the upper soil region (referred to as the &ldquo;near-field zone&rdquo;) and subtracting out the following deductions:</p>
    <ul>
    <li><strong>Life cycle emissions:</strong> Greenhouse gas emissions associated with the production, transport, handling, and spreading of ERW feedstock materials.</li>
    <li><strong>Counterfactual CDR:</strong> Carbon removal that would have occurred in the absence of the ERW project.</li>
    <li><strong>Near-field zone losses: </strong>Carbon losses that occur in the upper soil region.</li>
    <li><strong>Far-field zone losses: </strong>Carbon losses that occur on the path from the upper soil region to a durable carbon storage reservoir.</li>
    </ul>


