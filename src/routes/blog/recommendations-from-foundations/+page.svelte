	<p></p>
    <p>At the last EU Commission Expert Group on permanent carbon removals, the EU Commission signaled the start of a technical scoping process for an Enhanced Rock Weathering (ERW) methodology under the Carbon Removals and Carbon Farming (CRCF) regulation. This is a welcome step forward. The EU CRCF regulation will create a standardized and transparent EU-wide certification framework for carbon removals, representing a significant opportunity to raise the profile of ERW as a promising permanent carbon removal pathway and incorporate the most recent scientific developments into the first EU-wide ERW methodology.</p>
    <br>
    <p>Cascade&rsquo;s <a href="https://cascadeclimate.org/blog/foundations-for-carbon-removal-quantification-in-erw-deployments" target="_blank" rel="noopener"><em>Foundations for Carbon Dioxide Removal Quantification in ERW Deployments</em></a> provides a valuable starting point for developing an EU CRCF ERW methodology. This comprehensive framework represents the culmination of a collaborative, multi-stakeholder process involving approximately 50 academic scientists, 20 ERW project developers, and various not-for-profit organizations. <em>Foundations </em>represents the most current community-wide consensus on best practices for measurement, reporting, and verification (MRV) of net carbon dioxide removal in ERW projects.</p>
    <br>
    <p>We believe that <a href="https://cascadeclimate.org/blog/rigorous-mrv-methodologies" target="_blank" rel="noopener">rigorous MRV methodologies can and must be developed</a> for ERW and that a high quality methodology under the EU CRCF will send a clear signal to the market about minimum requirements for robust quantification and environmental health and safety.&nbsp;</p>
    <h2>What is enhanced rock weathering?</h2>
    <p>ERW involves spreading finely crushed silicate or carbonate rock&mdash;such as basalt, olivine, and limestone&ndash;onto fields, which speeds up the natural process of breaking down or &ldquo;weathering&rdquo; rocks by increasing their reactive surface area. Under many conditions, this weathering absorbs CO2 from the atmosphere, durably storing the carbon as bicarbonate in the ocean on the timescale of tens of thousands of years. ERW also has the potential to deliver agronomic benefits to farmers, such as <a href="https://www.mdpi.com/2073-445X/13/11/1839" target="_blank" rel="noopener">pH control</a>, <a href="https://journals.plos.org/plosone/article?id=10.1371/journal.pone.0295031" target="_blank" rel="noopener">improved nutrient availability</a>, and&mdash;through these benefits&mdash;increased crop yield.</p>


