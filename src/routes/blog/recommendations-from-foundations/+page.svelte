    <h3>2. Allow measurement flexibility for robust quantification</h3>

    <p>In many ERW deployments, multiple quantification methods&mdash;including measurements of the solid, liquid, and gas phases in the soil&mdash;are used to provide viewpoints into various aspects of the system. Current CDR quantification methodologies in the voluntary carbon market primarily allow for either soil core (solid phase) or porewater sampling (liquid phase) approaches. Additional measurement techniques, such as ion exchange resins and soil CO2 sensors, are currently under development. Regardless of measurement type, sampling density should be determined via a baseline site variability assessment with measurement uncertainty transparently accounted for in carbon credit calculations.</p>
    <br><p>Each method comes with its own technical and operational strengths and challenges, which are discussed in detail in <em>Foundations, </em>as well as <a href="https://www.frontiersin.org/journals/climate/articles/10.3389/fclim.2024.1345224/full" target="_blank" rel="noopener">Clarkson et al. (2023)</a>. The optimal measurement approach for accurately quantifying CDR depends on site-specific factors including soil type, regional climate, and agronomic management practices. <strong>We recommend that an EU CRCF methodology allow flexibility in measurement techniques to ensure robust CDR quantification across varying deployment conditions.&nbsp;</strong></p>
    <br>
    <h3>3. Safeguard against heavy metal accumulation</h3>

    <p>As the crushed rocks from ERW deployments break down over time and carbon is removed from the atmosphere, various metals within these rocks are also released. Accumulated metals can potentially cause environmental and human health risks at high concentrations. <em>Foundations</em> recommends every ERW deployment produce a health and safety risk assessment prior to the start of a project. Since <em>Foundations</em> was published, Cascade released an ERW <a href="https://cascadeclimate.org/blog/metal-accumulation-calculator" target="_blank" rel="noopener">metal accumulation calculator</a>, which uses intentionally conservative assumptions to compare potential accumulation of metals against existing regulations.&nbsp;</p>
    <br><p>In the EU CRCF context, <strong>we recommend requiring project developers to perform a conservative mass balance calculation using the baseline soil and feedstock measurements and soil conditions prior to application.</strong> This will assist in verifying that the proposed deployment will not cause soil metal concentrations to exceed the regulatory limits<strong> </strong>dictated by the EU <em>Council Directive on the Protection of the Environment, and in Particular of the Soil, when Sewage Sludge is used in Agriculture</em>(<a href="https://eur-lex.europa.eu/eli/dir/1986/278/oj/eng" target="_blank" rel="noopener"> Council Directive 86/278/EEC</a>) or the regulatory limits of the jurisdiction in which the project takes place.&nbsp;</p>
    <br><p><span style="color: #1d1c1d;">If baseline sampling indicates soil metal concentrations that are above the regulatory soil thresholds of the relevant jurisdiction, and the ERW deployment will exacerbate metal concentrations in soils, we recommend that the project developer select an alternative deployment site.</span></p>
    <br>
    <h3>4. Ensure methodology updates keep pace with scientific progress</h3>

    <p>While strong scientific foundations exist for ERW quantification today, we anticipate continued advancements that will lead to the evolution of MRV best practices over time. For example, there should be flexibility in the measurement requirements to allow for incorporation of new MRV approaches (e.g., the use of ion exchange resins) as they are proven out. <strong>We recommend an EU CRCF ERW methodology maintain enough flexibility to incorporate future scientific developments as part of a recurring review process </strong>(e.g., every two years).</p>
    <br><h2>What&rsquo;s next?</h2>
    <p>Building on the framework established in <em>Foundations</em>, the EU has the opportunity to write a high-quality, rigorous methodology for ERW. A well-designed CRCF methodology will establish clear standards for robust measurement and environmental safety, providing market certainty while ensuring ERW projects deliver genuine carbon removal with appropriate environmental safeguards.&nbsp;</p>
    <br><p>Cascade is committed to supporting this process through ongoing research and collaborative engagement. We are actively engaging the scientific community in exploring several critical areas relevant for advancing ERW methodologies, including quantification approaches for secondary silicate materials, rigorous intercomparison of measurement techniques, and understanding of soil organic carbon dynamics in ERW systems. Furthermore, we look forward to partnering with policymakers as they develop high rigor methodologies for ERW and build ERW into their net zero pathways.&nbsp;&nbsp;</p>
