	<main>
		<p></p>
		<p>
			2023 and 2024 represent a critical time in the development of agricultural enhanced rock
			weathering (ERW) as an industry and pathway. Increasingly large-scale field trials and
			deployments are beginning globally, major buyers are signing the first ERW offtake agreements,
			and new suppliers are entering the market at a rapid clip.
		</p>
		<p>
			As the market grows, it is increasingly important that we ensure that quantification of net
			carbon dioxide removal (CDR) through ERW is done in accordance with best available science.
			There are several unsettled topics related to quantification that the ERW community needs to
			align on early. Specifically, we need to:
		</p>
		<ul>
			<li>
				Align on the carbon and cation fluxes that must be accounted for in net CDR quantification,
				and a range of acceptable options to quantify these fluxes and associated uncertainties, based
				on current knowledge.
			</li>
			<li>
				Build clarity on the research agenda and data collection needs in the coming years to further
				improve our quantification approaches, and reduce key uncertainties.
			</li>
			<li>Create a transparent process to iterate future standards as science progresses.</li>
		</ul>
		<p>
			The process of gaining community consensus on what needs to be measured—and how—will accelerate
			the rate of progress for the field as a whole, while kickstarting a virtuous cycle of buyer
			confidence and deployment-led innovation.
		</p>
		<p>
			In light of the above, Cascade is coordinating a process to develop a community ERW
			quantification standard. This work-in-progress industry-wide standard will help ensure rigor and
			consistency in the measurement, reporting and verification (MRV) of ERW as market activity
			accelerates. As a foundational first step on the path toward a community quantification
			standard, 93 leaders in ERW—spanning scientists, suppliers, buyers, credit issuers, civil
			society and more—have publicly signed onto a set of <a
				target="_blank"
				href="https://drive.google.com/file/d/11jXl3baS5GWCy2C3I4O3AQ2GsvLxHbuI/view?usp=sharinghttps://drive.google.com/file/d/11jXl3baS5GWCy2C3I4O3AQ2GsvLxHbuI/view?usp=sharing"
				>Core Principles</a
			>. These Core Principles capture what we have agreed on as a community about the quantification
			of net carbon removal via ERW in agricultural soils. They also describe essential design
			principles for developing, evolving and stewarding the standard.
		</p>
		<p>
			In what follows below, we unpack the context surrounding this standard development effort: the
			rationale for a true ERW industry-wide community standard, how the community standard relates to
			credit issuers and protocol development, and the process by which the community standard will be
			developed, operationalized and stewarded.
		</p>
	
		<h2>Why a quantification standard?</h2>
		<p>
			Quantifying net carbon removal is inherently more challenging for ERW than for any closed-system
			carbon removal pathway (e.g. DAC), where carbon can often be directly “metered”. An end-to-end
			ERW carbon accounting requires the measurement or modeling of a series of carbon and cation
			fluxes across the soil column, groundwater, river systems, and oceans. Taking measurements in
			these extensively heterogeneous natural systems and combining them to assemble a comprehensive
			picture of net carbon removal is inherently complex.
		</p>
		<p>
			Thankfully we already know a great deal about quantifying ERW. This knowledge builds on decades
			of research quantifying natural weathering fluxes and, in recent years, field trials and
			pre-purchases of carbon dioxide removal tons via ERW. Yet many gaps in our understanding and
			toolkit must be addressed through further research. Evolving the way we measure and model all
			the various carbon fluxes and second-order impacts—quantifying the associated uncertainty,
			propagating error through the system, and discounting rigorously—will be an ongoing iterative
			improvement effort for many years.
		</p>
		<p>
			Meanwhile, <a target="_blank" href="/blog/foundations-for-a-healthy-erw-market-cycle"
				>recent developments</a
			> make clear that commercial activity in ERW is accelerating. Deployments will start scaling rapidly,
			making the next 1-2 years a critical crossroads for ERW. We have the opportunity to initiate a virtuous
			market cycle whereby rigor and consistency in quantification becomes the bedrock for buyer and public
			confidence—leading to growing deployment and learning-by-doing. Growing troves of on-field data will
			contribute to closing the uncertainty gaps in quantification, iteratively improving the standard.
		</p>
	
		<h2>What is a quantification standard?</h2>
		<p>
			The terms “standard”, “protocol”, and “methodology” are used inconsistently, carrying different
			meanings and implications for different people. We want to be explicit about what we mean, in
			the hopes of building toward a new and widely-agreed set of definitions.
		</p>
		<p>
			We use the term <strong>protocol</strong> to refer to a specific description of the measurements
			and calculations used to quantify net CDR for a specific supplier or approach. This includes a list
			of quantitative data and measurements to be taken at a specified sample density, how those samples
			will be processed, what models will be used, and how all of the measured and modeled output is synthesized
			to calculate tons removed and associated uncertainty. In other words, protocols are quite specific.
			They are a plan to follow; an order of operations.
		</p>
		<p>
			A <strong>quantification standard</strong> for ERW exists at a higher level than these protocols.
			While allowing for protocol diversity and not mandating any specific required approach to quantification,
			it enumerates the “common core” elements that must be consistent across all scientifically rigorous
			protocols.
		</p>
		<p>
			Some use “methodology” to refer to something akin to a standard: a set of guidelines defining
			what protocols or sets of measurements meet a high bar. For clarity, we will only use the term
			“standard.” You could think of a standard as “rules for evaluating a protocol”—removing
			ambiguity in determining whether a protocol is sufficiently rigorous.
		</p>
		<p>
			It’s also important to call out that a quantification standard is much narrower than a holistic
			crediting standard. Quantification standards only govern the quantification portions of a
			broader crediting standard, and are distinct from crediting rules. For example, quantification
			standards do not cover issues such as how credits are registered, issued, and retired, how to
			avoid double counting, how a validation and verification process is run, etc.
		</p>
		<p>
			Generally, our hope is to help the durable CDR industry move to a lexicon and market
			architecture that is in line with more established industries like financial accounting, food
			safety, fisheries management, etc. Developing true industry-wide standards for crediting—and
			within them, standards for carbon removal quantification that leading scientists agree reflect
			best available science—sit at the heart of this evolution. These industry-wide standards do not
			exist today.
		</p>
	
		<h2>What is a dynamic quantification standard?</h2>
		<p>
			The standard will also be explicitly dynamic—a version 1.0 that anticipates periodic iteration
			over time. We consider this a key design feature given the known, and potentially unknown, gaps
			in our scientific understanding. The standard will need to get updated as more data is
			collected, the science of ERW progresses, and our level of comfort with various quantification
			methods evolves.
		</p>
		<p>Specifically, we will need to develop clear processes for:</p>
		<ul>
			<li>Incorporating relevant new scientific research into following iterations.</li>
			<li>
				Gathering feedback from scientists, startups, governments, and other stakeholders between
				iteration cycles.
			</li>
			<li>Learning from deployment data collected under the previous version of the standard.</li>
		</ul>
	
		<h2>Whose standard?</h2>
		<p>
			The community ERW quantification standard will be a community good. The standard will be freely
			available and we welcome, encourage, and hope for the standard to be adopted by any existing
			credit issuers, suppliers, and buyers.
		</p>
		<p>
			In addition, it’s worth stressing again that there is no “Cascade Standard”. Cascade will never
			become a credit issuer nor a standards body. We want to partner with scientists, suppliers,
			buyers, credit issuers and governance bodies to build community alignment and bring the dream of
			a robust, rigorous, and consistent MRV system into reality. Cascade will help stand up the core
			building blocks of that system before handing those over to the right long-term stewards.
		</p>
	
		<h2>How will a true industry standard help the market?</h2>
		<p>
			The community quantification standard process is the first-of-it-kind in CDR standard
			development. This process is an explicitly different model than most carbon methodology
			processes to date (e.g. Verra methodologies), in which a credit issuer produces a standard
			principally for the use of minting credits on their own platform. Instead, we hope this standard
			can be used by buyers, suppliers, credit issuers, and academics across the entire field.
		</p>
		<p>
			As it currently stands, credit issuers like Verra, Puro, or Isometric, each create their own
			unique quantification methodology. The significant variability and absence of a “common core”
			across methodologies causes both large discrepancies in the MRV requirements that credit issuers
			impose upon suppliers and an understandable hesitation by CDR buyers as to whether all tons are
			equally rigorous and valid.
		</p>
	
		<img src={image1} alt="Current State" />
	
		<p>
			Due to the differing MRV requirements, suppliers might be incentivized to choose credit issuers
			with more lenient (and thus artificially lower-cost) MRV requirements, leading to a race to the
			bottom with quality. Buyers don’t know whose quality bar they should trust. Credit issuers and
			scientists both end up doing some degree of duplicative work, with the same scientific experts
			called upon in competing quantification methodology development processes.
		</p>
		<p>
			These are structural shortcomings, and no-one’s fault—yet they result in ERW MRV operating on
			quicksand rather than a strong foundation that instills public confidence.
		</p>
		<p>
			In the desired “after” state, the ERW community will enjoy a shared, industry-wide
			quantification standard. Credit issuers can freely adopt the community standard as the “common
			core” quantification requirements—while preserving the ability to customize and differentiate by
			wrapping around the “common core” additional methodological elements.
		</p>
	
		<img src={image2} alt="After 1.0" />
	
		<p>
			Under this model, our expectation is that the ERW market can transition towards operating with
			clear, consistent and industry-wide quantification rules of the road. Buyers can make
			commitments with confidence, and suppliers and credit issuers can operate their businesses with
			certainty knowing what must be quantified—and how—in order to generate valid carbon credits.
		</p>
	
		<h2>How will the standard be built?</h2>
		<p>
			Several core tenets guided the design of the community quantification standard development
			process:
		</p>
		<ul>
			<li>
				Inclusive, transparent, and high-integrity process that the community can feel proud of and
				confident in.
			</li>
			<li>
				Striving towards the development of an industry-wide standard with broad market adoption.
			</li>
			<li>
				Scrupulous avoidance of conflicts of interest. Funding for the community standard development
				process is entirely philanthropic, and the process is governed by a Steering Committee with no
				financial stake in ERW credit sales.
			</li>
			<li>
				Strong collaboration between industry and academic scientists. Academic and civil-society
				scientists should be the core engine for standard definition (again to minimize conflicts of
				interest), but must remain in constant conversation with market participants and
				industry-based scientists throughout.
			</li>
		</ul>
		<p>The standard is being developed through a multi-stage process involving two core tracks:</p>
		<ul>
			<li>
				Deep deliberation by a set of <strong>Working Groups (WGs)</strong> composed of research
				scientists free of any conflicts of interest. Five Working Groups will be covering the
				following topics
				<ul>
					<li>
						WG 1: Methods for measuring initial feedstock dissolution and alkalinity dynamics in the
						soil column
					</li>
					<li>
						WG 2: Constraining transient and permanent alkalinity sinks in the soil column and
						interactions with soil organic matter
					</li>
					<li>WG 3: Downstream evasion—catchments, rivers, oceans</li>
					<li>WG 4: Soil reactive transport models</li>
					<li>
						WG 5: Life cycle assessment, and best practice for ecological and community impact
						assessment
					</li>
					<li>Cross-WG: Consistent and rigorous uncertainty discounting</li>
				</ul>
			</li>
			<li>
				<strong>Industry Focus Groups</strong> and other forms of dialogue between market participants
				and the Working Groups. This dialogue solves for a) drawing on the expertise of the many excellent
				scientists who work at ERW suppliers; and b) ensuring that questions of operational feasibility
				and cost are properly addressed in the process of developing the standard.
			</li>
		</ul>
		<p>
			At the end of each stage in the process, sets of key outputs are generated that iterate
			progressively toward a fully-drafted standard.
		</p>
		<ul>
			<li>
				<strong>Preparation</strong> (Sep-early Nov 2023): A set of high-level core principles, describing
				key quantification components and guiding principles for the standard development process, were
				developed with consultation and feedback from across the community.
			</li>
			<li>
				<strong>Public knowledge synthesis and standard ideation</strong> (early Nov 2023-mid Jan 2024):
				A series of summaries capturing the state of public knowledge on key quantification questions and
				potential paths forward for the standard are being developed by the Working Groups. These summaries
				are iteratively refined through feedback from market practitioners, industry-based scientists,
				and other experts not serving on the Working Groups.
			</li>
			<li>
				<strong>Iterative standard consultation</strong> (mid Jan-early Mar 2024): A series of problem-solving
				sessions will bring together representatives from Working Groups and Industry Focus Groups to develop
				actionable solutions to questions raised or option sets proposed during the initial Working Group
				sessions. The Cascade team will begin drafting the standard based on the outcome of these iterative
				consultations.
			</li>
			<li>
				<strong>Standard decision deliberation</strong> (early Mar-Q2 2024): Proposed standard language
				will be brought back to the Working Groups for review and final decision deliberation.
			</li>
			<li>
				<strong>Full standard feedback and public consultation</strong> (Q2 2024): The full draft v1.0
				standard will be available for review and feedback by the entire ERW community before finalization
				in Q2 2024.
			</li>
		</ul>
	
		<h2>What happens after the v1.0 standard and how will the standard be implemented?</h2>
		<p>
			To integrate the v1.0 standard fully into the ERW market, a set of activities will happen in
			parallel to get and help stakeholders across the ecosystem to adopt the standard. This means
			working through questions and obstacles as buyers, suppliers, credit issuers align their
			operations with the standard. The first wave of suppliers, in particular, will require some
			support to close the gap between their current protocols and the v1.0 standard.
		</p>
		<p>
			A new independent mechanism will also need to be set up to assess the conformity of supplier
			protocols and credit-issuer methodologies with the standard. The goal here isn’t to “police”
			market activities, but rather to help create the engine that moves the market towards a world
			whereby all ERW deployments follow the same quantification requirements, and the public has the
			trust and confidence in the delivery claims.
		</p>
		<p>
			A standard update process needs to be designed to incorporate new best-available science at a
			pace in step with the learning rate of the market.
		</p>
		<p>
			Cascade will serve as an interim steward of the v1.0 standard, taking on the post-publication
			functions listed above for a time-limited period. As soon as possible, these functions will be
			handed over to a true long-term steward. We are already beginning to explore options for and lay
			the groundwork for this transition.
		</p>
