			<p>
				In November, 93 leaders in enhanced rock weathering (ERW)–including scientists, suppliers,
				buyers, credit issuers, civil society and more–publicly signed on to a set of <a
					target="_blank"
					href="https://drive.google.com/file/d/11jXl3baS5GWCy2C3I4O3AQ2GsvLxHbuI/view?usp=sharing"
					>Core Principles</a
				>
				on the quantification of net carbon removal via ERW. The Core Principles represent an ERW community-wide
				view on rigorous quantification and set a shared foundation at the outset of a community process
				to
				<a href="introducing-the-cqs"
					>develop the first version of a new ERW quantification standard</a
				>.
			</p>
			<p>
				The process to develop this standard explicitly brings together perspectives on best available
				science from across the ERW community, and it is conscientiously set up to avoid conflicts of
				interest. The decision engine for the community standard consists of five Working Groups made up
				of scientists and technical experts with no financial stake in issuing, selling and deploying
				ERW carbon credits. Their recommendations are iteratively updated and refined through successive
				rounds of feedback from suppliers, buyers, and other market participants. Funding for the
				community standard development process is entirely philanthropic, and the process is overseen by
				a Steering Committee similarly with no financial stake in ERW market activities.
			</p>
			<p>
				The quantification standard process comes at an important inflection point for the ERW market.
				Just earlier in December, the first-of-its-kind offtake purchase agreement in ERW was announced
				between Frontier Climate and Lithos Carbon. This is a significant first step, building a strong
				foundation for responsible procurement and rigorous measurement for ERW. What the community
				should hope for is a virtuous cycle of deployment-led innovation whereby rigor and consistency
				in quantification becomes the bedrock for buyer and public confidence–leading to growing
				deployment and learning-by-doing.
			</p>
