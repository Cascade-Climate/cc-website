		<p>
			The datasets vary in scope and measurement coverage. Each contribution includes contextual data alongside
			measurements from five categories. <strong>Solid-phase soil measurements and feedstock characterization dominate the collection</strong>, reflecting where commercial MRV efforts are most concentrated today. InPlanet&apos;s two Brazilian
			sites (Serra da Mantiqueira and Aracari) have the most variable coverage, spanning all five categories and
			complementing each other well. The Carbon Drawdown Initiative&apos;s XXL-lysimeter project contributes rich
			lysimetry data. Mati and Flux contribute data from especially data-sparse regions.
		</p>

		<h2 class="spaced-h2">How much do the datasets overlap?</h2>
		<p>
			Breadth is only half the story. To do <strong>synthesis science</strong> — pooling datasets to reach conclusions that no single
			project can support — datasets need to measure the same variables.
		</p>
		<p>
			Figure 2 shows how much the six datasets overlap, both as a raw count of shared measurement tags and as Jaccard
			similarity (shared tags divided by combined tags).
		</p>
		<figure class="figure-block">
			<img
				src="/images/blog/data-quarry-figure-2-overlap.png"
				alt="Heatmaps showing pairwise ERW Data Quarry dataset overlap by shared tag count and Jaccard similarity"
				class="figure-image figure-2-image"
				width="1153"
				height="2089"
				loading="lazy"
				decoding="async"
			/>
			<figcaption>
				<em
					>Figure 2. Pairwise dataset overlap, shown as shared tag count (top) and Jaccard similarity (bottom).</em
				>
			</figcaption>
		</figure>
		<p>
			There&apos;s a clear pattern. Total tag counts are high, but <strong>overlap between projects is limited.</strong> The strongest
			overlap is between Serra da Mantiqueira and Aracari (Jaccard 0.74) — InPlanet&apos;s two sites, which share a
			common measurement protocol. Cross-developer overlap is more modest. This is the expected consequence of a field
			in which each company has built its own MRV approach — operating across a spectrum of agricultural soils and
			management practices — and it constrains how much <strong>synthesis science</strong> the current catalog can support.
		</p>

		<h2 class="spaced-h2">What can the Data Quarry answer today?</h2>
		<p>
			When we launched the Data Quarry, we were especially interested in four questions that commercial data seemed
			well-positioned to inform:
		</p>
		<ul>
			<li>
				<strong>Soil organic carbon (SOC):</strong> how does an ERW project affect SOC over time?
			</li>
			<li><strong>Dissolution rates:</strong> how and where does rock dissolve well?</li>
			<li><strong>Time lags in MRV:</strong> what are CDR lags and what are true losses?</li>
			<li>
				<strong>Health and safety:</strong> how do ERW projects affect potentially toxic element accumulation in
				fields?
			</li>
		</ul>
		<p>
			<strong>None of these questions can be fully answered by today&apos;s collection</strong> — but each is being moved forward,
			with more visibility than ever before. Here&apos;s where the data shows today.
		</p>
		<ul>
			<li>
				<strong>Dissolution.</strong> This is where the catalog contributes most today. The datasets provide
				dissolution measurements from a broad, international sample — numbers now available to the wider research
				community rather than held in silos.
			</li>
			<li>
				<strong>Soil organic carbon.</strong> Resolving SOC dynamics will require denser and longer-term sampling
				than a single year of project data can provide. Currently four datasets have SOC data (mean n = 127, sd =
				155). As the depth and breadth of this data expands, so will our ability to interrogate SOC trajectories.
			</li>
			<li>
				<strong>MRV time lags.</strong> One year of data isn&apos;t enough to characterize MRV time lags. But every
				additional project year brings these questions closer — year-two data is expected from at least one project
				before the end of 2026.
			</li>
			<li>
				<strong>Potentially toxic elements.</strong> The current data show no evidence of regulatory violations in any
				of the projects in the Data Quarry. Foreclosing toxicity risks entirely, however, will require longer-term
				monitoring. All potentially toxic element data is made public and can be accessed at
				<a href="https://cdrxiv.org/channels/erw-data-quarry" target="_blank" rel="noopener noreferrer"
					>our channel on CDRXIV</a
				>.
			</li>
		</ul>
		<p>
			These challenges aren&apos;t shortcomings of the Data Quarry itself — they&apos;re the current shape of ERW field
			data. And showing that shape clearly is part of what the Quarry is for.
		</p>

		<h2 class="spaced-h2">What&apos;s next for the Data Quarry?</h2>
		<p>
			The catalog will keep growing on two fronts. We will continue to onboard new datasets, with over 15 datasets in
			the queue, widening the range of soils, climates, and feedstocks represented. And we will soon begin ingesting
			the second year of data from several datasets already in the Quarry — the first step toward the longer time
			series that questions like time lags and SOC changes require.
		</p>

		<h2 class="spaced-h2">How can I use these datasets today?</h2>
		<p>
			This is the first set of commercial ERW project data available to the research community — six datasets, four
			continents, characterized in depth and ready to work with today. If you study dissolution, feedstock behavior,
			or field-scale ERW across diverse soils and climates, there is something here for you, and the collection is only
			growing.
		</p>
