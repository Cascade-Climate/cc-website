		<section id="scopeofWork">
			<h1>Scope of Work</h1>
			<div>Within super pollutant mitigation, we are building a bridge from catalytic, voluntary climate finance toward a policy-driven end state where accountability for super pollutant reductions is embedded within industry value chains and national greenhouse gas inventories. This strategy will require consistency and rigor in the accounting of short-lived climate pollutants in a framework that is built around CO2, a long-lived climate pollutant.

                Credible accounting is the foundation for building confidence in the climate impact of any given intervention. We believe it is critical for the climate community to develop a shared mental model for accounting and claims-making across short-lived and long-lived mitigation activities, and a sense for the pros, cons, and implementation implications of the primary accounting option sets. 
                
                Why this is important:
                
                The range of GHG accounting approaches being considered is currently expanding rather than converging, creating risks of fragmentation, market uncertainty, and eroded confidence in mitigation efforts.
                Market participants require clarity of options (and pros and cons for each) on how credits can be used and the “why” behind the investment case.
                Getting to the desired end-state will require strong early project proof points to put in front of policymakers and industry, and providing clear and consistent rules of the road across project types will accelerate speed-to-action and help to mitigate accounting and market uncertainty-based paralysis. 
                
                Questions within this scope of work include: 
                <ul>
                <li>Should market-based actors consider changing accounting and claims rules to better value near term warming?</li>
                <li>Do super pollutant reduction credits have a role in achieving net zero targets? If so, how?</li> 
                <li>Should you allow compensation of CO2 emissions with super pollutant mitigation actions?</li>
                </ul>
