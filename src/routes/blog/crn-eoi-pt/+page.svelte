    <section id="desafio">
        <h1>O Desafio
            </h1>
        <p>ERW é uma estratégia de remoção de dióxido de carbono que envolve a disseminação de matérias-primas de rocha triturada alcalina (incluindo silicatos, carbonatos ou outros materiais alcalinos) em solos agrícolas. A quantificação da remoção de carbono em uma implantação de ERW é complexa e envolve uma série de fluxos biogeoquímicos Uma breve explicação sobre o ERW pode ser encontrada <a href="https://cascadeclimate.org/Cascade%20Climate_Enhanced%20Rock%20Weathering%20101_vFinal.pdf" target="_blank" rel="noopener">aqui</a>.</p>

        <p>
            O trabalho rigoroso realizado por um ecossistema crescente de partes interessadas e abordagens está impulsionando o progresso real na área de ERW. No entanto, ainda existe a necessidade de pesquisa coordenada em uma variedade de características de locais, com delineamentos experimentais e estratégias de medição consistentes e mutuamente comparáveis, para responder de forma eficiente às principais questões enfrentadas pelo setor.</p>


    </section>

	<section id="objetivo">
        <h1>O Objetivo</h1>
        <p>A Coordinated Research Network é a nossa resposta a esse desafio. O objetivo é estabelecer até 15 locais de pesquisa de campo altamente padronizados para o estudo de ERW em climas subúmidos ou mais úmidos. Estamos planejando a implementação de um ou dois desses locais em 2026, para os quais já garantimos financiamento filantrópico para um período de cinco anos.</p>

        <p>A CRN pretende responder às seguintes perguntas:</p>
        <ol style="margin: 0;">
            <li>Em uma variedade de solos e climas, qual é o potencial de remoção de carbono pelo ERW e em quais escalas de tempo? Estudos de campo produziram estimativas divergentes<sup>1,2,3,4,7,8</sup>, e as diferenças nas estratégias de medição, objetivos, relatórios de dados e condições dos locais dificultam a obtenção de conclusões amplas.
            </li>
            <li>Como diferentes estratégias de quantificação de remoção de dióxido de carbono (por exemplo, medição de mudanças na química do solo, medição de mudanças na química da água porosa, uso de resinas de troca iônica) podem nos ajudar a entender o panorama completo do intemperismo da matéria-prima e do transporte de carbono? A implantação desses métodos lado a lado ajudará a esclarecer as complexidades do processo de intemperismo e a comparar diferentes métodos de medição.
            </li>
            <li>Quais são os benefícios agrícolas do ERW e quais fatores os impulsionam? Os impactos de produtividade relatados<sup>1,5,6</sup> variam de insignificantes a drásticos, mas os estudos muitas vezes carecem de controles adequados ou poder estatístico para isolar os mecanismos subjacentes a esses efeitos.</li>
            <li>O que acontece com os produtos do intemperismo em solos mais profundos? Reações abaixo das profundidades comumente medidas podem reverter a remoção de carbono, mas essas dinâmicas são difíceis de se observar e mal representadas nos modelos.</li>
            <li>Nesses locais, a rede tentará conciliar diferentes estratégias de medição — fase sólida, fase aquosa e resinas de troca iônica — e para monitorar o destino dos cátions em solos mais profundos (~1m), onde permanece uma incerteza significativa. Parâmetros agronômicos também serão medidos em todos os locais para melhor entender quais mecanismos levam a melhorias observadas na produtividade das culturas. As medições serão voltadas para restringir de forma suficiente a variabilidade dos principais parâmetros do solo de modo a apoiar o desenvolvimento, a calibração, a validação e a intercomparação de modelos geoquímicos.</li>
        </ol>

    </section>

