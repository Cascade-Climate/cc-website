		<section id="background">
			<h1>Background</h1>
			<p>
				In recent years, enhanced rock weathering (ERW) has quickly moved from a niche research topic to a widely
				discussed pathway for durable carbon dioxide removal. Academic interest has surged, peer-reviewed
				publications have multiplied, and philanthropic and commercial actors have supported research and early
				deployments. ERW has also drawn attention from large innovation prize programs such as the
				<a
					href="https://www.xprize.org/prizes/carbonremoval"
					target="_blank"
					rel="noopener noreferrer">XPrize Carbon Removal competition</a
				>, and is being considered in policy frameworks, including the IPCC National GHG Inventories, the European
				Union's certification frameworks for carbon removal, and climate-smart agricultural programs in the U.S. and
				Brazil.
			</p>
			<p>
				This attention is well-deserved: ERW holds the potential to remove atmospheric CO₂ while delivering tangible
				soil health and crop nutrient benefits. Some of the early ERW momentum is emerging in tropical agricultural
				regions in the Global South, especially where soil nutrient depletion is chronic and climate stress is growing
				— making the link between carbon removal, productivity, and farmer livelihood especially compelling. As
				Cascade's
				<a href="https://cascadeclimate.org/blog/weathering-potential-explorer">Weathering Potential Explorer</a>
				highlights, weathering rates and
				agricultural benefits align in many of these regions.
			</p>
			<p>But alongside this promise, some questions remain that limit ERW's growth:</p>
			<ul>
				<li>How quickly and reliably does carbon removal occur?</li>
				<li>
					What are the best and most cost-effective ways to measure carbon removal in ERW in real-world agricultural
					systems?
				</li>
				<li>How do carbon removal and agronomic outcomes of ERW vary across soils, climates, and rock types?</li>
			</ul>
			<p>
				These uncertainties are not a sign that ERW lacks potential — they reflect a field that is still maturing. But
				for ERW to move toward climate-relevant scale deployments, building a strong and trusted evidence base is
				essential.
			</p>
		</section>

		<section id="climate-relevant-scale">
			<h1 data-nav-title="Climate-Relevant Scale">The Requirements for Reaching Climate-Relevant Scale</h1>
			<p>
				Most of the early activity in ERW is linked to the voluntary carbon market (VCM). The VCM has played an
				important catalytic role, supporting early field deployments and funding the needed investment in the
				development of measurement, monitoring and verification approaches.
			</p>
			<p>
				But the voluntary market alone will not determine whether ERW becomes a large-scale climate solution. In the
				long term, ERW will need additional support from several complementary pathways, including:
			</p>
			<ul>
				<li>
					<strong>Compliance carbon markets</strong>, where removals could be recognized under a range of national and
					international crediting frameworks, each with distinct quantification and durability requirements.
				</li>
				<li>
					<strong>Pay-for-practice agricultural programs</strong>, where farmers are rewarded for adopting practices that
					provide proven climate and soil benefits at a landscape scale.
				</li>
				<li>
					<strong>Farmer-driven adoption</strong>, where ERW becomes attractive to farmers based on agronomic performance
					and economic returns.
				</li>
			</ul>
			<p>Each of these demand pathways requires different types of evidence.</p>
			<p>
				Compliance markets tend to set a higher evidentiary bar than the VCM — but not a uniform one. Article 6
				bilateral agreements, regional cap-and-trade systems, and national crediting mechanisms each apply different
				standards for quantification, additionality, and durability, but share a common orientation toward
				jurisdictional credibility and tonne-for-tonne integrity.
			</p>
			<p>
				Pay-for-practice programs require the ability to estimate climate benefits reliably and at a low cost, in ways
				that can integrate into national greenhouse gas inventories and the IPCC accounting frameworks used to track
				countries' climate commitments. They also typically require evidence of agronomic benefits — soil health, crop
				yield, input efficiency — that justify adoption as a farming practice, not just a carbon intervention.
			</p>
			<p>
				And critically, since ERW is a farming practice, farmers will ultimately determine whether it spreads across
				landscapes. This means that agronomic performance — and the individual farmer's benefit-risk calculus and
				practical experience of ERW — will matter just as much as carbon accounting.
			</p>
			<p>To reach climate-relevant scale, ERW must demonstrate viability for all three of these pathways.</p>
		</section>

		<section id="commercial-questions">
			<h1 data-nav-title="Commercial Gaps">The Questions Commercial Actors Can't Answer</h1>
			<p>
				Commercial deployment plays an important role in advancing the evidence base for ERW. These deployments
				generate valuable real-world data, and the companies leading these efforts have meaningfully advanced
				measurement, monitoring and verification approaches. Data sharing efforts such as our
				<a href="https://cascadeclimate.org/blog/data-sharing-system-for-erw">ERW Data Quarry</a> initiative aim to
				ensure that insights from commercial
				deployments contribute to the broader scientific understanding of ERW.
			</p>
			<p>
				However, commercial deployments are shaped by the opportunities in front of them — today, that means the
				voluntary carbon market. As such, research embedded within commercial deployments is often designed to support
				near-term carbon credit validation, rather than a broader set of questions needed to unlock long-term policy
				and adoption pathways. Many of the most important scientific questions require a different type of research
				effort, including:
			</p>
			<ul>
				<li>Measurements beyond those that are easily measured, including deeper soil profiles and downstream river systems</li>
				<li>Higher measurement density, including more frequent and more costly sampling</li>
				<li>Agronomic outcomes, including crop productivity and soil health</li>
				<li>Dedicated long-term monitoring, often spanning many years</li>
			</ul>
			<p>
				These types of studies are difficult for commercial actors to prioritize and sustain. Commercial deployments
				face limited timelines, cost pressure, and evolving business priorities that make long-term, focused research
				efforts challenging. Early commercial projects also concentrate on large scale farm operations, where rock
				deployments and monitoring are more tractable — leaving important questions about ERW's impact on smallholder
				systems less explored.
			</p>
			<p>
				In addition, there are structural constraints associated with commercially-led research. Commercial projects are
				often designed to demonstrate success, and results that are null, negative, or inconclusive may be less likely to
				be surfaced or shared. Independent research complements commercial deployments, helping ensure that a fuller
				range of outcomes are observed and compared.
			</p>
			<p>
				Recently, several high-profile carbon removal startups in other fields have shut down or scaled back operations,
				underscoring the need for multi-year scientific research programs that outlive the
				<a
					href="https://carbonherald.com/running-tide-shuts-down-citing-lack-of-demand-from-the-voluntary-market/"
					target="_blank"
					rel="noopener noreferrer">uncertain trajectories</a
				> of early companies. To complement commercial
				<a href="https://cascadeclimate.org/blog/foundations-for-a-healthy-erw-market-cycle"
					>deployment-driven learning</a
				>, the field needs independent research infrastructure that can
				persist beyond individual companies and answer questions private entities are least able to address.
			</p>
			<p>
				If ERW is to develop a robust and trusted scientific foundation, independent research efforts will be essential
				— particularly for the questions that commercial deployments are least able to address.
			</p>
		</section>

		<section id="coordinated-research">
			<h1 data-nav-title="Coordinated Research">The Need for Coordinated Research</h1>
			<p>
				Academic researchers around the world are already tackling many important questions for ERW. But the current
				research landscape remains fragmented. Individual studies may use different experimental designs and measurement
				strategies, making results difficult to compare or synthesize.
			</p>
			<p>
				At the same time, much of the existing academic research has been concentrated in regions where funding is most
				readily available — primarily in North America and Europe. Yet much of ERW's potential lies elsewhere,
				particularly in tropical agricultural regions across the Global South.
			</p>
			<p>
				Advancing the scientific foundations of ERW will require research that is coordinated across sites, comparable
				in experimental design, focused on under-represented geographies, and able to measure both carbon and agronomic
				outcomes on the same fields.
			</p>
			<p>
				When research is coordinated, the insights from many studies can add up to far greater than the sum of their
				parts. This is why researchers across carbon removal fields are beginning to develop infrastructure that builds
				scientific confidence. For example, the
				<a href="https://www.carbontosea.org/" target="_blank" rel="noopener noreferrer">Carbon to Sea Initiative</a> is
				supporting coordinated, multi-site, long-term monitoring programs for ocean alkalinity enhancement, recognizing
				that such efforts are essential for building scientific confidence.
			</p>
		</section>

		<section id="introducing-bedrock">
			<h1 data-nav-title="Bedrock Initiative">Introducing the Bedrock Initiative</h1>
			<p>
				Since our founding, Cascade Climate has helped lead the work to validate ERW for scale. We see the Bedrock
				Initiative as a continuation of that commitment to ERW's future.
			</p>
			<p>
				Bedrock Initiative will critically strengthen the evidence base for ERW — generating research more transparent
				than commercial projects, more coordinated than individual studies, and built to complement both. Bedrock
				focuses on the questions that individual actors are structurally least able to tackle — longer time horizons,
				broader geographic coverage, and rigorous assessment of agronomic outcomes. Specifically, the Bedrock Initiative
				will tackle three problems inhibiting deployment and demand:
			</p>

			<h2>Closing the carbon-removal uncertainty gap</h2>
			<p>
				Bedrock's Coordinated Research Network (CRN) will apply standardized research protocols across intensively measured field sites — generating the apples-to-apples evidence the field has lacked. Sustained over
				multi-year timescales, measurements will go deeper into the soil profile and use multiple methods to robustly
				constrain ERW's carbon removal under varied conditions.
			</p>

			<h2>Building the agronomic evidence farmers and policymakers need</h2>
			<p>
				Studies at Scale will address questions that can only be answered on working farms — including soil organic carbon
				dynamics, watershed-level monitoring, and yield benefits across cropping systems. With particular focus on the
				Global South, where ERW's promise for acidic soils is greatest, rigorous trials will build the
				crop-benefit data needed to earn farmer trust.
			</p>

			<h2>Driving down monitoring costs that block scale</h2>
			<p>
				The Model Acceleration effort will convene leading academic geochemical modelers to identify key field data gaps
				and develop validated, model-based monitoring — much as reliable weather models have replaced costly ground
				stations.
			</p>

