		<section id="compliance-markets">
			<h2>Compliance Markets: Enabling Scale</h2>
			<p>
				Voluntary projects that have demonstrated LRM carbon credits that are technically feasible and financially
				viable can open pathways to compliance-grade carbon mechanisms. Under Article 6.2 of the Paris Agreement,
				countries can enter bilateral agreements to transfer carbon credits known as ITMOs for use toward national
				climate targets.
			</p>
			<p>
				Compliance markets bring three things to LRM that voluntary markets cannot: longer time horizons, larger and
				more predictable demand, and a stronger and more bankable signal to industry actors.
			</p>
			<figure class="inserted-photo">
				<img src="/images/blog/jcm-f-gas-recovery-vietnam.png" alt="LRM case study visual" />
			</figure>
		</section>

		<section id="industry-leaders">
			<h2>Industry Leaders: Driving Economies of Scale</h2>
			<p>
				For industry actors, participation in LRM is often framed as voluntary goodwill. In practice, engagement is a
				function of economics: when recovery is happening at meaningful scale, when logistics networks exist for
				aggregating refrigerants, and when the commercial case for reclamation or certified destruction is clear,
				industry actors have both the incentive and the infrastructure to participate.
			</p>
			<p>
				When investment materializes, the impact goes beyond capital. An OEM that requires verified refrigerant recovery
				as a condition of warranty service changes technician behavior at scale. A distributor that builds take-back
				infrastructure into refrigerant sales operations creates a structural return pathway that does not depend on
				technician initiative or carbon credit prices.
			</p>
			<figure class="inserted-photo">
