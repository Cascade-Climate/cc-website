			<h1>Introduction</h1>
			<p>
				The mechanics of LRM — leak prevention, recovery, reclamation, and destruction — are straightforward. Building
				the economic and institutional ecosystem required to implement those activities at scale is not. Across the LRM
				value chain, from technicians who need recovery machines, to aggregators who must build trucking and collection
				routes, to facilities capable of reclamation or destruction, the constraint is often the same: there is no
				reliable way to pay for it.
			</p>
