			<h1 data-nav-title="Cascade's 2026 Roadmap">Cascade&apos;s 2026 Roadmap: Testing the Theory In Practice</h1>
			<h2>Ensuring a Foundation of High-Quality, Fit-for-Purpose Voluntary LRM Methodologies</h2>
			<p>
				As methodologies for HFC destruction in Article 5 countries evolve, the field faces a foundational challenge:
				proving that these first projects deliver verifiable climate impacts and avoid unintended outcomes.
			</p>
			<p>
				Cascade is actively engaged in this methodology development process, including convening cross-sector workshops
				and contributing feedback on methodology drafts under review.
			</p>
			<h2>Demand and Supply Aggregation for Initial Catalytic Voluntary Transactions</h2>
			<p>
				Cascade is working to connect motivated voluntary buyers with early-stage LRM projects in the Global South,
				including priority countries in Southeast Asia. The goal is to generate not only credits but also high-quality
				data and operational proof points needed to support transition into compliance systems.
			</p>
			<h2>Engaging Compliance Market Buyers and Industry Value Chain Leaders</h2>
			<p>
				Cascade is building relationships with sovereign compliance buyers and engaging project developers, HVAC OEMs,
				and chemical distributors to align around high-integrity rulesets and operational readiness.
			</p>
			<h2>Supporting Effective Policies</h2>
			<p>
				Cascade&apos;s policy analysis and engagement runs in parallel with voluntary and compliance market work, with a
				focus on understanding enabling conditions for LRM policy in priority Southeast Asian countries and identifying
				concessional finance opportunities where carbon markets are not the right fit.
			</p>
