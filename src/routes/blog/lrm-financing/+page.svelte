		<section id="policy">
			<h2>Policy: Codifying What Markets Demonstrate</h2>
			<p>
				Voluntary climate finance and compliance carbon market mechanisms can build operational foundations, but without
				a policy framework that embeds LRM as a legal and institutional norm, those foundations remain dependent on
				external finance and private initiative.
			</p>
			<p>
				This is where carbon finance has a distinct and time-limited role to play: absorbing some initial
				infrastructure buildout costs while policy frameworks are being constructed and resourced. Carbon finance can
				function as a bridge rather than a permanent substitute.
			</p>
			<p>
				Policy defines the endgame: a well-designed regulatory framework eventually reduces reliance on carbon finance
				and establishes LRM as a durable part of national cooling infrastructure.
			</p>
