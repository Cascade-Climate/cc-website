			<h1>What Are We Building Toward?</h1>
			<p>
				The <a href="https://ozone.unep.org/kigali-amendment-overview" target="_blank" rel="noopener noreferrer">Kigali Amendment to the Montreal Protocol</a> sets a clear directional trajectory: HFCs are phased down over
				time, and lower-GWP alternatives replace them. Unfortunately, transition alone does not solve all of the
				emissions problems. HFC refrigerants already contained inside billions of air conditioning units and
				refrigeration systems will continue circulating, leaking, and being vented for decades. Under the Kigali
				Amendment, developing countries, referred to as Article 5 parties, have a delayed phasedown timetable, meaning
				that HFC banks in these countries will continue to grow over the next several years. This underscores Cascade’s
				focus on LRM in Article 5 countries in Southeast Asia.
			</p>
			<p>
				Sustained, economically viable LRM practices are the ultimate goal. Within LRM, the highest certainty-of-impact
				action to mitigate emissions from legacy high-GWP refrigerants is destruction. A single kilogram of R-410A, an
				HFC widely used in commercial and residential air-conditioning equipment, contains a global warming potential
				2,088 times that of CO<sub>2</sub>. Recovering and destroying these gases permanently eliminates their warming
				potential from the atmosphere.
			</p>
			<p>
				The overarching need is for refrigerant recovery infrastructure, including trained technicians with appropriate
				equipment and access to reclamation or destruction facilities. The challenge is not identifying what needs to
				happen, it is building functioning recovery markets in places where none currently exist.
			</p>
		</section>

		<section id="theory-of-change">
			<h1 data-nav-title="Building Recovery Markets">Building Recovery Markets: A Theory of Change</h1>
			<p>
				Multiple mechanisms can support LRM, including carbon markets, compliance finance, industry investments, and
				policy. These mechanisms can operate individually or in parallel as market readiness and policy timelines
				allow. They can also be highly effective when sequenced as stages in an intentional, system-building process,
				where one mechanism supports the foundation for the next.
			</p>
			<p>
				The underlying logic is cumulative: policy is most effective when it can build on demonstrated feasibility, and
				capital flows most reliably into markets whose economics have already been proven at the project level.
			</p>
		</section>

		<section id="voluntary-carbon-financing">
			<h2>Voluntary Carbon Financing: The Zero-to-One Entry Point</h2>
			<p>
				In the absence of regulation or policy incentives, refrigerant recovery has no natural revenue stream. It
				requires significant investment in equipment, trained technicians, logistics, and destruction or reclamation.
				Voluntary carbon markets can help close this incentive gap by monetizing the climate value of destroyed
				refrigerants that would otherwise be vented.
			</p>
			<p>
				As with all carbon markets, crediting integrity matters enormously. LRM carbon credits are only as valuable as
				the measurement behind them and require rigorous additionality assessments, refrigerant-specific emissions
				factors, and documented chain of custody for collected gases from recovery through destruction.
			</p>
			<p>
				Voluntary markets also have real limits: carbon credit prices and trends can be volatile and buyer-dependent,
				and no country can anchor a national LRM system on voluntary demand from private corporations alone.
			</p>
